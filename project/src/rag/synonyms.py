"""도메인 동의어 사전 기반 질의 확장 (Query Expansion).

RAG에서 검색이 실패하는 흔한 원인은 어휘 불일치(vocabulary mismatch)다:
사용자는 "부작용"이라 묻지만 규제문서는 "이상사례"라고 쓴다.
TF-IDF/BM25 같은 어휘 기반 검색은 이 간극을 스스로 못 메우므로,
제약 규제 도메인 용어 사전으로 질의를 확장해 회수(recall)를 보강한다.

설계 원칙:
  - 확장은 1단계 하이브리드 검색(넓게 회수)에 전가중으로 적용하고,
    2단계 리랭킹에서는 '원 질의' 토큰이 주 신호, 확장 토큰은 절반 가중의
    보조(aux) 신호다(retriever 참고) — 확장어가 정밀도 신호를 희석하지
    않도록 회수/정밀 단계에서 가중을 차등한 것.
  - 사전은 '사용자 구어 → 문서 정식 용어' 방향으로만 확장한다(단방향).
    양방향/연쇄 확장은 노이즈(주제 표류)를 만들기 쉽다.
  - 표제어 매칭의 경계(v8): 한글 표제어는 부분 문자열("심각하게"←"심각")이
    의도된 동작이지만, 영문 표제어까지 부분 문자열로 보면 무관 단어 안에서
    발화한다 — "audit finding"의 'ind', "PVC 포장재"의 'pv'가 각각 임상시험·
    약물감시 확장을 일으켜 주제 표류로 이어졌다. 영문/숫자 표제어는 단어
    경계(비영숫자 양측)에서만 매칭한다.
  - LLM 질의 재작성 대비: 결정론적(감사 가능)·저지연·무비용. 실무에선
    이 사전을 현업 용어집(신입 교육자료·SOP 용어 정의)에서 추출해 관리한다.
"""
from __future__ import annotations

import re

# 사용자 구어/약어 → 규제문서 정식 용어 (단방향)
SYNONYMS: dict[str, list[str]] = {
    # 약물감시(PV) — 현업이 가장 자주 섞어 쓰는 축
    "부작용": ["이상사례", "이상반응"],
    # eval 오류 분석에서 발굴한 구어 축: "부작용이 심각하게…" 류 질의가
    # 규정 용어('중대한 이상사례')와 전혀 겹치지 않아 검색이 실패했다.
    "심각": ["중대한"],
    "약물감시": ["시판 후 안전관리", "이상사례"],
    "pv": ["약물감시", "시판 후 안전관리"],
    "pharmacovigilance": ["약물감시"],
    "신속보고": ["중대한 이상사례 보고"],
    "psur": ["정기적 최신 안전성 정보 보고서", "정기보고"],
    # 표시기재/라벨링
    "라벨": ["표시기재", "첨부문서"],
    "라벨링": ["표시기재"],
    "설명서": ["첨부문서"],
    # 허가/등록 약어
    # ("dmf"→"원료의약품 등록"은 eval에서 정의 섹션(개요)으로 주제 표류를
    #  일으켜 제외 — 문서 자체가 이미 약어를 병기하는 용어는 확장하지 않는다)
    "ind": ["임상시험계획 승인"],
    "인허가": ["품목허가"],
    # 품질(GMP)
    "밸리데이션": ["적격성평가"],
    "데이터 무결성": ["데이터 완전성"],
    # eval 오류 분석 축: "제품이 섞이는 것" 같은 구어는 GMP 정식 용어
    # (교차오염·혼동)와 겹치지 않는다. 현업 구어→정식 용어 매핑으로 보강.
    "섞이": ["교차오염", "혼동"],
    "섞임": ["교차오염", "혼동"],
}


def _term_in(term: str, low: str) -> bool:
    """표제어 매칭 — 영문/숫자 표제어는 단어 경계, 한글은 부분 문자열.

    한글은 조사·활용이 직결되므로("심각하게") 부분 매칭이 맞고, 영문은
    합성어 내부 매칭("finding"⊃"ind", "pvc"⊃"pv")이 오탐이라 경계가 맞다 —
    같은 규칙을 양쪽에 쓰면 어느 한쪽이 반드시 깨지는 비대칭 지점(v8).
    """
    if re.fullmatch(r"[a-z0-9 ]+", term):
        return re.search(rf"(?<![a-z0-9]){re.escape(term)}(?![a-z0-9])", low) is not None
    return term in low


def expand_query(query: str) -> str:
    """질의에 도메인 동의어를 덧붙여 반환한다(원 질의는 항상 보존).

    질의 안에 사전 표제어가 등장하면 그 정식 용어를 뒤에 이어 붙인다.
    덧붙이기(append) 방식이라 원 질의 신호는 그대로 남고,
    BM25/TF-IDF 양쪽에서 확장어가 추가 매칭 기회를 만든다.
    """
    low = query.lower()
    extra: list[str] = []
    for term, expansions in SYNONYMS.items():
        if _term_in(term, low):
            for e in expansions:
                if e.lower() not in low and e not in extra:
                    extra.append(e)
    if not extra:
        return query
    return f"{query} {' '.join(extra)}"
