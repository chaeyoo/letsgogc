"""개인정보 비식별화 (PII Redaction) — 외부 API 경계의 안전장치.

PV 케이스 서술에는 환자 개인정보(주민번호·연락처·이름 등)가 섞여 들어온다.
이 텍스트가 그대로 외부 LLM API·로그로 나가면 개인정보보호법/GDPR 위반이자
제약사 보안 정책 위반이다. 그래서 에이전트 입구에서 먼저 마스킹한다.

설계 원칙:
  - 규칙(정규식) 기반: 결정론적이라 '무엇이 마스킹되는가'를 감사할 수 있고,
    LLM 호출 '이전'에 실행되므로 마스킹 자체를 LLM에 의존하는 순환이 없다.
  - 원문 비보존: 마스킹 결과에는 유형·건수만 남기고 원 값은 어디에도 저장하지 않는다.
  - 규제문서 검색에 PII 토큰은 노이즈일 뿐이므로, 마스킹이 답변 품질을 해치지 않는다.
  - 한계: 정규식은 자유 서술 속 이름 전부를 못 잡는다. 실무 확장 지점은
    NER 기반 비식별화(예: Presidio)이며, 이 모듈이 그 교체 자리다.
"""
from __future__ import annotations

import re
from dataclasses import dataclass

# (유형, 패턴, 대체 라벨) — 한국 제약 현장에서 흔한 식별자 위주
#
# 경계는 \b 가 아니라 숫자 룩어라운드다 — 한글도 \w 라서 "주민번호는 …7입니다",
# "010-…-5678로 연락" 처럼 조사·명사가 직결된(한국어에서 가장 흔한) 표기에서는
# \b 경계가 성립하지 않아 매칭 전체가 통째로 빠진다. 검증기의 날짜 추출이 같은
# 이유로 룩어라운드로 전환됐는데(verifier._DATE_RE), 그 교훈이 마스킹 계층에는
# 적용되지 않은 비대칭이 사각지대였다 — 검증기의 비검출은 경고 누락이지만
# 마스킹의 비검출은 곧 유출이라 실패 비용이 더 크다.
_PATTERNS: list[tuple[str, re.Pattern[str], str]] = [
    (
        # 구분자 없는 표기("9001011234567")도 잡는다 — 하이픈만 요구하면 붙여
        # 쓴 주민번호가 그대로 외부 API·로그로 나간다(마스킹은 과탐이 미탐보다
        # 싼 안전장치 — 13자리 오탐은 가려질 뿐이지만 미탐은 유출이다).
        # 성별코드는 [1-8] — [1-4]로 좁히면 외국인등록번호(5~8)가 완전한
        # 식별번호인 채로 통째로 경계를 통과한다(v8).
        # 구분자에 점(.)을 허용한다(v9) — 전화 패턴은 점 표기("010.1234.5678")를
        # 받는데 주민번호만 [-–] 로 좁혀 "900101.1234567" 이 통과하던, 같은 파일
        # 안의 구분자 비대칭(비대칭은 곧 우회로)을 대칭화.
        "주민등록번호",
        re.compile(r"(?<!\d)\d{6}\s*[-–.]?\s*[1-8]\d{6}(?!\d)"),
        "[주민번호]",
    ),
    (
        # 점 구분 표기("010.1234.5678")도 흔하다 — 구분자 어휘가 좁으면 그만큼이 우회로다.
        # v8: en-dash(–)·국제표기("+82 10-…", 국가번호 뒤 0 생략)·괄호 지역번호("(02)345-…")
        # 를 추가 — 주민번호 패턴은 –를 받는데 전화만 못 받던 같은 파일 안의 구분자
        # 비대칭이 곧 우회로였다.
        "전화번호",
        re.compile(
            r"(?<!\d)(?:"
            r"(?:\+82[-–.\s]?0?1[016789]|01[016789])[-–.\s]?\d{3,4}[-–.\s]?\d{4}"  # 휴대폰(+82 포함)
            r"|\(?0\d{1,2}\)?[-–.\s]?\d{3,4}[-–.\s]?\d{4}"  # 유선(괄호 지역번호 포함)
            r")(?!\d)"
        ),
        "[전화번호]",
    ),
    (
        # 로컬·도메인은 ASCII 로 한정한다 — [\w.+-] 는 한글도 삼켜서
        # "메일은 hong@x.com입니다" 의 '입니다'까지 마스킹에 빨려 들어간다
        # (값은 가려지니 유출은 아니지만, 마스킹 리포트가 원문 범위를 왜곡해
        # 감사가 어려워진다 — 과잉 매칭도 경계 결함이다).
        "이메일",
        re.compile(r"[A-Za-z0-9._%+-]+@[A-Za-z0-9-]+(?:\.[A-Za-z0-9-]+)*\.[A-Za-z]{2,}"),
        "[이메일]",
    ),
    (
        # "환자 번호: A-1023"처럼 띄어 쓴 표기도 잡는다(v8) — 붙여 쓴 표기만
        # 받으면 공백 하나가 우회로다.
        "환자/차트번호",
        re.compile(r"(환자\s?번호|차트\s?번호|등록\s?번호)\s*[:#]?\s*[A-Za-z0-9-]+"),
        r"\1 [번호]",
    ),
]

# '홍길동님/씨' 형태의 호칭 붙은 이름(경량 근사; 실무는 NER로 교체).
# 호칭이 붙지만 이름이 아닌 일반 명사는 스톱리스트로 오탐을 막는다.
#
# 경계(v8): 호칭 '뒤'에도 한글 직결 표기가 온다 — "홍길동님이 호소",
# "김철수씨가 방문"처럼 조사가 붙는 형태가 한국어에서 가장 흔한데,
# (?=[^가-힣]|$) 는 이를 전부 놓쳤다. 전화·주민번호가 숫자 룩어라운드로
# 봉합한 바로 그 사각지대(위 _PATTERNS 주석)가 이름 정규식에는 남아 있던
# 비대칭.
#
# 설계 반전(v9): v8 은 조사 '첫 글자'의 허용 집합([이가은는…])으로 열었는데,
# 조사는 열린 활용 집합이라("…님으로부터"·"…님께서"·"…님처럼"·"…님보다"·
# "…님마저"·"…님조차"·"…님밖에") 집합 밖 첫 글자마다 유출이 재발한다 —
# 마스킹은 미탐이 곧 유출이므로 열거로 좁히는 방향이 항상 진다. 호칭 뒤는
# 무엇이 오든 매칭하고(룩어헤드 제거), 호칭이 붙지만 이름이 아닌 일반
# 명사·직함의 오탐은 스톱리스트로 방어한다(마스킹은 과탐이 미탐보다 싸다).
_NAME_RE = re.compile(r"([가-힣]{2,4})\s?(님|씨)")
_NAME_STOP = {
    "선생", "고객", "담당자", "관리자", "여러분", "사장", "부장", "과장", "팀장",
    "간호사", "약사", "의사",
    # 호칭 앞에 오는 일반 호칭어(이름 아님) — 조사 허용으로 매칭 면적이 넓어진
    # 만큼 오탐 방어도 함께 넓힌다(v8)
    "어머", "아버", "사모", "스승", "아가", "도련",
    # 빈출 직함·직위(v9) — "교수님이 문의"의 [이름] 오마스킹은 감사 리포트를
    # 왜곡하고 보고자 직함(자격) 정보까지 지운다. 오탐도 경계 결함으로 센다.
    "교수", "박사", "원장", "실장", "이사", "대표", "회장", "소장", "차장", "본부장",
}


@dataclass
class RedactionReport:
    text: str                       # 마스킹된 텍스트
    counts: dict[str, int]          # 유형별 마스킹 건수 (원 값은 절대 담지 않음)

    @property
    def redacted(self) -> bool:
        return bool(self.counts)

    def summary(self) -> list[dict]:
        """API/UI 노출용 요약 — 유형과 건수만."""
        return [{"type": t, "count": c} for t, c in self.counts.items()]


def merged_summary(*reports: RedactionReport) -> list[dict]:
    """여러 입력 필드의 마스킹 리포트를 유형별 합산 요약으로 합친다.

    도구의 자유 텍스트 인자가 여러 개면(케이스 서술·보고자·환자 정보) 각각을
    마스킹하되, 사용자에게 보이는 리포트는 하나여야 한다 — 필드별로 흩어지면
    "무엇이 마스킹됐는가"의 감사가 어려워진다.
    """
    counts: dict[str, int] = {}
    for rep in reports:
        for kind, n in rep.counts.items():
            counts[kind] = counts.get(kind, 0) + n
    return [{"type": t, "count": c} for t, c in counts.items()]


def redact(text: str) -> RedactionReport:
    """텍스트에서 PII 를 마스킹하고 유형별 건수를 보고한다."""
    counts: dict[str, int] = {}
    for kind, pattern, repl in _PATTERNS:
        text, n = pattern.subn(repl, text)
        if n:
            counts[kind] = counts.get(kind, 0) + n

    def _mask_name(m: re.Match[str]) -> str:
        if m.group(1) in _NAME_STOP:
            return m.group(0)
        counts["이름(호칭)"] = counts.get("이름(호칭)", 0) + 1
        return f"[이름]{m.group(2)}"

    text = _NAME_RE.sub(_mask_name, text)
    return RedactionReport(text=text, counts=counts)
